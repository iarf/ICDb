export function cmToIn(input: number): number {
    return input * 0.393701;
}
export function kgToLb(input: number): number {
    return input * 2.20462;
}
export function barToPsi(input: number): number {
    return input * 14.5038;
}
export function lToCuft(input: number): number {
    return input * 0.0353147;
}